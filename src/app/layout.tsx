import type { Metadata } from 'next'
import { Geist, Geist_Mono } from 'next/font/google'
import { Toaster } from '@/components/ui/sonner'
import { SITE_NAME, SITE_DESCRIPTION, getAppUrl } from '@/lib/site'
import './globals.css'

