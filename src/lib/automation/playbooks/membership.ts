const U = I.UNIVERSAL
const ANY = M.ANY

// Configuración editable mínima (Documento Maestro): común a todos.
const EDITABLE = [
  'nombre', 'descripcion', 'triggers', 'reglas', 'vigencia', 'renovacion',
  'beneficios', 'promociones', 'prioridad', 'variables', 'horarios', 'segmentos',
  'sucursales', 'canales', 'idioma', 'limites',
] as const

// KPIs típicos del ciclo de vida de membresías.
const MEM_KPIS = [
  'activaciones', 'tasa_renovacion', 'tasa_cancelacion', 'upgrades', 'downgrades',
  'uso_membresia', 'ltv_miembro', 'ingresos_recurrentes', 'reactivaciones',
  'permanencia_promedio', 'tasa_apertura', 'tasa_clic',
] as const

/** Helper: completa los apartados comunes de un playbook de membresías. */
function pb(
  p: Omit<AutomationPlaybook, 'category' | 'editable' | 'kpis' | 'engines' | 'compatibleMembershipModels'> &
    Partial<Pick<AutomationPlaybook, 'engines' | 'kpis' | 'editable' | 'compatibleMembershipModels'>>,
): AutomationPlaybook {
  return {
    category: 'membresias',
    editable: p.editable ?? EDITABLE,
    kpis: p.kpis ?? MEM_KPIS,
    engines: p.engines ?? ['rule', 'action', 'membership', 'analytics'],
    compatibleMembershipModels: p.compatibleMembershipModels ?? [ANY],
    ...p,
  }
}

