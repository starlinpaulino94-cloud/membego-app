/**
 * Biblioteca de Automation Playbooks de MEMBRESÍAS (Fase E1.6). Automatizan el
 * CICLO DE VIDA COMPLETO de cualquier membresía: creación → activación → primer
 * uso → uso recurrente → cambios → renovación → upgrade/downgrade → suspensión →
 * reactivación → cancelación → expiración → historial. No automatizan un plan
 * concreto (Silver/Gold/…): funcionan con CUALQUIER modelo del Membership Engine
 * (Unlimited, Créditos, Family, Fleet, Corporate, Seasonal, Hybrid, Subscription,
 * Wallet/Prepaid, por niveles, por consumo, personalizada…). Un modelo futuro se
 * integra por configuración, sin cambiar código.
 *
 * Estrategias probadas de suscripción/membresía (Amazon Prime/Costco/gimnasios/
 * SaaS/clubes) convertidas en playbooks UNIVERSALES instalables sobre el
 * Automation Engine (E1). Reutilizan Rule Engine (condiciones), Action Engine
 * (acciones) y el Membership Engine (estado/vigencia/límites), y referencian
 * Benefit/Promotion/Reward/Campaign por código.
 *
 * Cada Playbook es una automatización real (`config`) + su documentación
 * completa (25 apartados, incl. "Tipos de membresías compatibles"). Nada
 * hardcodeado por plan: los beneficios/planes se referencian por variable/código.
 */

