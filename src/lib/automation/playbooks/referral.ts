const U = I.UNIVERSAL
const ANY = R.ANY

// Configuración editable mínima (Documento Maestro): común a todos.
const EDITABLE = [
  'nombre', 'descripcion', 'triggers', 'reglas', 'recompensas', 'condiciones',
  'segmentos', 'prioridad', 'variables', 'horarios', 'canales', 'idioma',
  'limites', 'presupuesto', 'duracion', 'vigencia',
] as const

// KPIs típicos de programas de referidos.
const REF_KPIS = [
  'invitaciones_creadas', 'invitaciones_compartidas', 'tasa_conversion_referido',
  'referidos_registrados', 'referidos_convertidos', 'recompensas_liberadas',
  'fraude_detectado', 'valor_por_referido', 'embajadores_activos', 'roi_referidos',
  'tasa_apertura', 'tasa_clic',
] as const

/** Helper: completa los apartados comunes de un playbook de referidos. */
function pb(
  p: Omit<AutomationPlaybook, 'category' | 'editable' | 'kpis' | 'engines' | 'compatibleReferralModels'> &
    Partial<Pick<AutomationPlaybook, 'engines' | 'kpis' | 'editable' | 'compatibleReferralModels'>>,
): AutomationPlaybook {
  return {
    category: 'referidos',
    editable: p.editable ?? EDITABLE,
    kpis: p.kpis ?? REF_KPIS,
    engines: p.engines ?? ['rule', 'action', 'referral', 'analytics'],
    compatibleReferralModels: p.compatibleReferralModels ?? [ANY],
    ...p,
  }
}

