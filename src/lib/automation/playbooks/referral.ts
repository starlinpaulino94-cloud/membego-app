/**
 * Biblioteca de Automation Playbooks de REFERIDOS (Fase E1.7). Automatizan el
 * JOURNEY COMPLETO de un programa de referidos: código generado → compartido →
 * invitado registrado → primera conversión → validación antifraude → liberación
 * de recompensa → ranking → escalamiento → embajadores/influencers. NO
 * reimplementan el Referral Engine (que ya define lógica, reglas y recompensas):
 * lo operan y coordinan con el resto de motores. Funcionan con CUALQUIER modelo
 * de programa (clásico, ambos ganan, solo invitado, solo quien invita,
 * progresivo, embajadores, influencers, corporativo, empleados, equipos,
 * afiliados…). Un modelo futuro se integra por configuración.
 *
 * Estrategias probadas de referral (Dropbox/Uber/PayPal/marketplaces)
 * convertidas en playbooks UNIVERSALES instalables sobre el Automation Engine
 * (E1). Reutilizan Rule Engine (condiciones/antifraude), Action Engine
 * (acciones), el Referral Engine (estado/recompensas) y referencian Benefit/
 * Promotion/Membership/Reward/Campaign/Gamification/Analytics por código. La
 * recompensa por un referido puede activar cualquier otro motor existente.
 *
 * Cada Playbook es una automatización real (`config`) + su documentación
 * completa (25 apartados, incl. "Tipos de programas compatibles"). Nada
 * hardcodeado por modelo: recompensas/programas por variable/código.
 */

